package a3_estrutura_repetitiva;

 

public class C2_for {
	public static void main(String[] args) {
		
		System.out.println("Contagem progressiva");
		for(int i = 0;i <= 10;i++) {
			System.out.print(" - " + i);
		}
		System.out.println();
		System.out.println("Contagem Regressiva");
		for(int i = 10; i >= 0;i --) {
			System.out.print(" - " + i);
		}
		 
	}
}
