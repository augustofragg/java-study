public class A3_1_ProcessDados {
	public static void main(String[] args) {
		double b, B, h,area;
		
		b = 6.0;
		B = 8.0;
		h = 5.0;
		
		area = (b + B)/2.0*h;
		
		System.out.println(area);
		
		float b1, B1, h1, area1;
		
		b1 = 6f;
		B1 = 8f;
		h1 = 5f;
		
		area1 = (b1 + B1)/2f * h1;
		
		System.out.println(area1);
	}
}
